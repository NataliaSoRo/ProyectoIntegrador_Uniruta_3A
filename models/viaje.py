class Viaje:

    def __init__(self, id=None, origen=None, destino=None, fecha=None, hora=None, unidad=None, estatus=None):
        self.id = id
        self.origen = origen
        self.destino = destino
        self.fecha = fecha
        self.hora = hora
        self.unidad = unidad
        self.estatus = estatus